

name = 'Test user'
age = 25

# .format() method
print('Hello all, my name is {} & I am {} years old'.format(name,age))
print('Hello all, my name is {0} & I am {1} years old'.format(name,age))
print('Hello all, my name is {r} & I am {e} years old'.format(r=name,e=age))

# f-string method
print(f'Hello all, my name is {name} & I am {age} years old')



